package test.main; // package 예약어를 이용해서 페키지 표시

//클래스 식별자(이름) 은 MainClass 입니다.
public class MainClass {//클래스의 시작
	
	// run 했을때 실행순서가 들어오는 main 메소드(method) 정의하기
	public static void main(String[] args) {//메소드의 시작
		System.out.println("main 메소드가 시작 됩니다.");
		System.out.println("하나");
		System.out.println("두울");
		System.out.println("세엣");
		System.out.println("main 메소드가 종료 됩니다.");
	}//메소드의 끝
	
}//클래스의 끝








