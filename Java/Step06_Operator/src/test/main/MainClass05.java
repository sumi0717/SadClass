package test.main;
/*
 *  [ 연산자 ]
 *  
 *  5. 대입 연산자
 *  
 *  종류 : =, +=, -= , *=, /=, %=
 */
public class MainClass05 {
	public static void main(String[] args) {
		int num=10;
		
		num = num+2;
		//위의 표현식을 줄이면
		num += 2;
		
		num = num-3;
		//위의 표현식을 줄이면
		num -= 3;
		
		num = num*4;
		//위의 표현식을 줄이면
		num *= 4;
		
		num = num/5;
		//위의 표현식을 줄이면
		num /= 5;
		
		num = num%6;
		//위의 표현식을 줄이면
		num %= 6;		
	}
}







